package Day2_Java;

public class Loops {

	public static void main(String[] args) {
		int n = 5;
		for(int i=0; i <= n; i++) {
			System.out.println("Hello Team !!!!!");
		}
		
		
		while(n != 2) {
			System.out.println("Working Fine....");
			n--;
		}
		
		do {
			System.out.println("How Are You????");
			n++;
		} while(n < 8);

	}

}
