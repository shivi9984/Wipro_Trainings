package Day9_Wrapper_Collection;

public class Wrapper2 {

	public static void main(String[] args) {
		String s = "20";
		int i = Integer.parseInt(s);    // Converting String to Integer.(Auto-boxing)
		System.out.println(i);
		
		double a = Double.parseDouble(s);   // String to double (Auto-boxing)
		System.out.println(a);
		
		float b = Float.parseFloat(s);      // String to float  (Auto-boxong)
		System.out.println(b);
		
		

	}

}
